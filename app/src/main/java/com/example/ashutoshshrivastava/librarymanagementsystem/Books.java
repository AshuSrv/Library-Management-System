package com.example.ashutoshshrivastava.librarymanagementsystem;

public class Books {
        String booknameText;
        String authorText;
        String ratingText;
        String genreText;
        String coverPicTextURL;
        String issuerText;
        String statusText;

    public Books() {

    }

        public String getBooknameText() {
            return booknameText;
        }

        public void setBooknameText(String booknameText) {
            this.booknameText = booknameText;
        }

        public String getAuthorText() {
            return authorText;
        }

        public void setAuthorText(String authorText) {
            this.authorText = authorText;
        }

        public String getRatingText() {
            return ratingText;
        }

        public void setRatingText(String ratingText) {
            this.ratingText = ratingText;
        }

        public String getGenreText() {
            return genreText;
        }

        public void setGenreText(String genreText) {
            this.genreText = genreText;
        }

        public String getCoverPicTextURL() {
            return coverPicTextURL;
        }

        public void setCoverPicTextURL(String coverPicTextURL) {
            this.coverPicTextURL = coverPicTextURL;
        }

        public String getIssuerText() {
            return issuerText;
        }

        public void setIssuerText(String issuerText) {
            this.issuerText = issuerText;
        }

        public String getStatusText() {
            return statusText;
        }

        public void setStatusText(String statusText) {
            this.statusText = statusText;
        }


}
